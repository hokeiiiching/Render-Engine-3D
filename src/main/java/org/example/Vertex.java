package org.example;
// A structure to store our three coordinates (X, Y, Z)
public class Vertex {
    public double x;
    public double y;
    public double z;

    // Constructor

    public Vertex(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

}

// x : left-right on screen
// y : up-down on screen
// z : perpendicular to screen
